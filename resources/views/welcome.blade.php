			<!--Ciri-Ciri Khas-->
			<h2 id="toc2">
				Ciri-Ciri Khas
			</h2>
			<p class="paragraf">
				Ciri yang segera mudah dikenali pada tumbuhan adalah warna kehijauan tapi bisa kuning yang dominan akibat kandungan pigmen klorofil yang berperan vital dalam proses penangkapan energi melalui fotosintesis. Dengan demikian, tumbuhan secara umum bersifat autotrof. Beberapa perkecualian, seperti pada sejumlah 
				<a href="https://id.wikipedia.org/wiki/Tumbuhan_parasit" target="_blank" title="Tumbuhan parasit adalah tumbuhan yang untuk kelangsungan hidupnya menggantungkan sebagian atau seluruh sumber energinya pada tumbuhan lain (disebut tumbuhan inang) dan mengakibatkan inangnya mengalami kekurangan energi.">tumbuhan parasit</a>, 
				merupakan akibat adaptasi terhadap cara hidup dan lingkungan yang unik. Karena sifatnya yang autotrof, tumbuhan selalu menempati posisi pertama dalam rantai aliran energi melalui organisme hidup (<a href="https://id.wikipedia.org/wiki/Rantai_makanan" target="_blank" title="Rantai makanan adalah perpindahan energi makanan dari sumber daya tumbuhan melalui seri organisme atau melalui jenjang makan.">rantai makanan</a>).
			</p>
			<p class="paragraf">
				Tumbuhan bersifat stasioner atau tidak bisa berpindah atas kehendak sendiri, meskipun beberapa alga hijau bersifat motil (mampu berpindah) karena memiliki 
				<a href="https://id.wikipedia.org/wiki/Flagelum" target="_blank" title="Flagelum (jamak flagela) adalah alat gerak (motile organ) berbentuk cambuk pada sejumlah organisme bersel satu.">flagelum</a>. 
				Akibat sifatnya yang pasif ini tumbuhan harus beradaptasi secara fisik atas perubahan lingkungan dan gangguan yang diterimanya. Variasi 
				<a href="https://id.wikipedia.org/wiki/Morfologi" target="_blank" title="Morfologi dipakai oleh berbagai cabang ilmu.">morfologi</a> 
				tumbuhan jauh lebih besar daripada anggota kerajaan lainnya. Selain itu, tumbuhan menghasilkan banyak sekali 
				<a href="https://id.wikipedia.org/wiki/Metabolit_sekunder" target="_blank" title="Metabolit sekunder adalah senyawa metabolit yang tidak esensial bagi pertumbuhan organisme dan ditemukan dalam bentuk yang unik atau berbeda-beda antara spesies yang satu dan lainnya.">metabolit sekunder</a> 
				sebagai mekanisme pertahanan hidup atas perubahan lingkungan atau serangan pengganggu. Reproduksi juga terpengaruh oleh sifat ini.
			</p>
			<p class="paragraf">
				Pada tingkat seluler, dinding sel yang tersusun dari 
				<a href="https://id.wikipedia.org/wiki/Selulosa" target="_blank" title="Selulosa merupakan senyawa organik dengan rumus (C6H10O5)n, sebuah polisakarida yang terdiri dari rantai linier dari beberapa ratus hingga lebih dari sepuluh ribu ikatan &beta;(1&rarr;4) unit D-glukosa.">selulosa</a>, 
				<a href="https://id.wikipedia.org/wiki/Hemiselulosa" target="_blank" title="Hemiselulosa merujuk pada polisakarida yang mengisi ruang antara serat-serat selulosa dalam dinding sel tumbuhan.">hemiselulosa</a>, 
				dan 
				<a href="https://id.wikipedia.org/wiki/Pektin" target="_blank" title="Pektin merupakan segolongan polimer heterosakarida yang diperoleh dari dinding sel tumbuhan darat.">pektin</a> 
				menjadi ciri khasnya, meskipun pada tumbuhan tingkat sederhana kadang-kadang hanya tersusun dari pektin. Hanya sel tumbuhan yang memiliki 
				<a href="https://id.wikipedia.org/wiki/Plastid" target="_blank" title="Plastid adalah salah satu organel pada sel-sel (tumbuhan dan alga).">plastida</a>; 
				juga 
				<a href="https://id.wikipedia.org/wiki/Vakuola" target="_blank" title="Vakuola merupakan ruang dalam sel yang berisi cairan (cell sap dalam bahasa Inggris) yang berupa rongga yang diselaputi membran (tonoplas).">vakuola</a> 
				yang besar dan seringkali mendominasi volume sel.
			</p>
