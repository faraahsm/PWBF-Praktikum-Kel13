				<p class="paragraf">
					Kebanyakan 
					<a href="https://id.wikipedia.org/wiki/Alga" target="_blank" title="Alga (jamak Algae) adalah sekelompok organisme autotrof yang tidak memiliki organ dengan perbedaan fungsi yang nyata.">alga</a> 
					sudah tidak lagi dimasukkan ke dalam 
					<a href="https://id.wikipedia.org/wiki/Kerajaan_(biologi)" target="_blank" title="Dalam biologi, kerajaan (bahasa Inggris:Kingdom; Latin:regnum, pl. regna) adalah tingkatan paling atas dari tingkatan klasifikasi makhluk hidup.">Kerajaan</a> 
					Plantae. Alga terdiri dari beberapa kelompok yang berbeda dari organisme yang menghasilkan energi melalui fotosintesis, yang masing-masing muncul secara terpisah-pisah dari leluhur yang non-fotosintetik. Alga yang paling mencolok adalah 
					<a href="https://id.wikipedia.org/wiki/Gulma_laut" target="_blank" title="Gulma laut, ganggang laut, atau rumput laut merupakan salah satu sumber daya hayati yang terdapat di wilayah pesisir dan laut.">gulma laut</a>, 
					alga multiseluler yang mungkin kurang lebih mirip tanaman terestrial, tetapi diklasifikasikan bersama 
					<a href="https://id.wikipedia.org/wiki/Alga_hijau" target="_blank" title="Alga hijau adalah kelompok alga berdasarkan zat warna atau pigmentasinya.">alga hijau</a>, 
					<a href="https://id.wikipedia.org/wiki/Alga_merah" target="_blank" title="Alga merah atau Rhodophyta adalah salah satu filum dari alga berdasarkan zat warna atau pigmentasinya.">merah</a>, 
					dan 
					<a href="https://id.wikipedia.org/wiki/Alga_coklat" target="_blank" title="Alga coklat, alga pirang, atau Phaeophyceae adalah salah satu kelas dari dari alga Heterokontophyta.">coklat</a>. 
					Masing-masing kelompok alga ini juga termasuk berbagai jenis 
					<a href="https://id.wikipedia.org/wiki/Makhluk_hidup" target="_blank" title="Dalam setiap pernyataan suatu kejadian menurut sistem biologi dan ekologi, organisme (bahasa Yunani:organon yang berarti alat) adalah kumpulan molekul-molekul yang saling memengaruhi sedemikian sehingga berfungsi secara stabil dan memiliki sifat hidup.">organisme</a> 
					mikroskopik dan organisme uniseluler.
				</p>
				<p class="paragraf">
					Sebagian besar alga kemudian juga mulai dipisahkan dari keanggotaan tumbuhan karena mereka tidak memiliki diferensiasi jaringan dan tidak membentuk 
					<a href="https://id.wikipedia.org/wiki/Klorofil" target="_blank" title="Klorofil (dari bahasa Inggris, chlorophyll) atau zat hijau daun (terjemah langsung dari bahasa Belanda, bladgroen) adalah pigmen yang dimiliki oleh berbagai organisme dan menjadi salah satu molekul berperan utama dalam fotosintesis.">klorofil</a> 
					sebagai 
					<a href="https://id.wikipedia.org/wiki/Pigmen" target="_blank" title="Pigmen atau zat warna adalah zat yang mengubah warna cahaya tampak sebagai akibat proses absorpsi selektif terhadap panjang gelombang pada kisaran tertentu.">pigmen</a> 
					penangkap energi.
				</p>
				<p class="paragraf">
					Penggunaan teknik-teknik 
					<a href="https://id.wikipedia.org/wiki/Biologi_molekular" target="_blank" title="Biologi molekular atau biologi molekul merupakan salah satu cabang biologi yang merujuk kepada pengkajian mengenai kehidupan pada skala molekul.">biologi molekuler</a> 
					terhadap 
					<a href="https://id.wikipedia.org/wiki/Filogeni" target="_blank" title="Dalam biologi, filogeni atau filogenesis adalah kajian mengenai hubungan di antara kelompok-kelompok organisme yang dikaitkan dengan proses evolusi yang dianggap mendasarinya.">filogeni</a> 
					tumbuhan ternyata memberikan banyak dukungan atas pemisahan ini. Tumbuhan dalam arti yang sekarang dipakai (arti sempit) dianggap sebagai keturunan dari suatu 
					<a href="https://id.wikipedia.org/wiki/Alga_hijau" target="_blank" title="Alga hijau adalah kelompok alga berdasarkan zat warna atau pigmentasinya.">alga hijau</a> 
					purba.
